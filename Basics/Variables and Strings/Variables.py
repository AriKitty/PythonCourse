# numericals
a, b = 12, 3
print(a + b)  # 15
print(a - b)  # 9
print(a * b)  # 36
print(a / b)  # 4.0
print(a // b)  # 4
print(a % b)  # 0

# Strings
parrot = "Norwegian Blue"
print(parrot)
print(parrot[3])
print(parrot[-1])  # Counts backwards from the end
print(parrot[0:6])  # Prints starting at index 0 and up to but not including index 6
print(parrot[:6])  # Prints everything up to index 6
print(parrot[6:])  # Prints index 6 and everything after
print(parrot[-4:-2])  # Prints from the 4th character from the end to the 2nd character from the end
print(parrot[0:6:2])  # Between the 0th and 6th index, print every 2nd character, starting with the first